/*
  Type conversion (typecasting) vs Type coesion

  * Alteração de um tipo de dado para outro tipo
*/

// type coesion (o próprio js muda)
console.log("9" + 5);
// transformou o 5 (number) para string

// type conversion (fazendo manualmente)
console.log(Number("9") + 5);
// transformou o o 9 (string) para number
